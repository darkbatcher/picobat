int Dos9_CmdPause(char* lpLine)
{
	lpLine = Dos9_SkipBlanks(lpLine);

	if (!strncmp(lpLine+6, "/?", 2)) {

		Dos9_ShowInternalHelp(DOS9_HELP_PAUSE);
		return 0;
	}

	fputs(lpMsgPause, fOutput);
	fputs(DOS9_NL, fOutput);
	Dos9_Getch();

	return 0;
}
